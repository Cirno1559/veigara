package com.hxzy.entity.sales;

import java.io.Serializable;

/**
 * @author 
 */
public class Vehicleinfo implements Serializable {
    /**
     * 汽车销售编号
     */
    private Integer vehicleInforId;

    /**
     * 汽车类型
     */
    private String vehicleInforVersion;

    /**
     * 汽车名称
     */
    private String vehicleInforName;

    /**
     * 外键 厂商ID
     */
    private Manufacturer manufacturer;

    /**
     * 单价
     */
    private Integer vehicleInforPrice;

    /**
     * 汽车状态
     */
    private String vehicleInforState;

    /**
     * 汽车图片
     */
    private String vehicleInforImage;

    private static final long serialVersionUID = 1L;

    public Integer getVehicleInforId() {
        return vehicleInforId;
    }

    public void setVehicleInforId(Integer vehicleInforId) {
        this.vehicleInforId = vehicleInforId;
    }

    public String getVehicleInforVersion() {
        return vehicleInforVersion;
    }

    public void setVehicleInforVersion(String vehicleInforVersion) {
        this.vehicleInforVersion = vehicleInforVersion;
    }

    public String getVehicleInforName() {
        return vehicleInforName;
    }

    public void setVehicleInforName(String vehicleInforName) {
        this.vehicleInforName = vehicleInforName;
    }

    

    public Manufacturer getManufacturer() {
		return manufacturer;
	}

	public void setManufacturer(Manufacturer manufacturer) {
		this.manufacturer = manufacturer;
	}

	public Integer getVehicleInforPrice() {
        return vehicleInforPrice;
    }

    public void setVehicleInforPrice(Integer vehicleInforPrice) {
        this.vehicleInforPrice = vehicleInforPrice;
    }

    public String getVehicleInforState() {
        return vehicleInforState;
    }

    public void setVehicleInforState(String vehicleInforState) {
        this.vehicleInforState = vehicleInforState;
    }

    public String getVehicleInforImage() {
        return vehicleInforImage;
    }

    public void setVehicleInforImage(String vehicleInforImage) {
        this.vehicleInforImage = vehicleInforImage;
    }

  
}