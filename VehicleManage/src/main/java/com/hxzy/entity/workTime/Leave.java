package com.hxzy.entity.workTime;

import java.io.Serializable;
import java.util.Date;

/**
 * @author 
 */
public class Leave implements Serializable {
    /**
     * \请假编号
     */
    private Integer leaveId;

    /**
     * 外键
     */
    private Integer adminId;

    /**
     * 请假开始时间
     */
    private Date leaveBegin;

    /**
     * 请假结束时间
     */
    private Date leaveEnd;

    /**
     * 请假统计
     */
    private Integer leaveTotal;

    private static final long serialVersionUID = 1L;

    public Integer getLeaveId() {
        return leaveId;
    }

    public void setLeaveId(Integer leaveId) {
        this.leaveId = leaveId;
    }

    public Integer getAdminId() {
        return adminId;
    }

    public void setAdminId(Integer adminId) {
        this.adminId = adminId;
    }

    public Date getLeaveBegin() {
        return leaveBegin;
    }

    public void setLeaveBegin(Date leaveBegin) {
        this.leaveBegin = leaveBegin;
    }

    public Date getLeaveEnd() {
        return leaveEnd;
    }

    public void setLeaveEnd(Date leaveEnd) {
        this.leaveEnd = leaveEnd;
    }

    public Integer getLeaveTotal() {
        return leaveTotal;
    }

    public void setLeaveTotal(Integer leaveTotal) {
        this.leaveTotal = leaveTotal;
    }
}